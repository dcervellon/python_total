# En Python, el método lower() se utiliza para convertir una cadena de texto en letras minúsculas. Esto significa que todas las letras mayúsculas en la cadena se convertirán en letras minúsculas y las letras minúsculas existentes en la cadena permanecerán sin cambios. Puedes aplicar este método a una cadena de caracteres de la siguiente manera:

text = "This is a text of Mateo Cervellon"

result = text.lower()
print(result)

result = text[1].lower()
print(result)

result = text[10:14].lower()
print(result)
