# En Python, el método upper() se utiliza para convertir una cadena de texto en letras mayúsculas. Esto significa que todas las letras minúsculas en la cadena se convertirán en letras mayúsculas y las letras mayúsculas existentes en la cadena permanecerán sin cambios. Puedes aplicar este método a una cadena de caracteres de la siguiente manera:


text = "This is a text of Mateo Cervellon"

result = text.upper()
print(result)

result = text[1].upper()
print(result)

result = text[10:14].upper()
print(result)
