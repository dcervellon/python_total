# En Python, un "float" (abreviatura de "floating-point number", número en coma flotante en español) es un tipo de dato utilizado para representar números con decimales o números reales. Los números en coma flotante pueden tener una parte entera y una parte fraccional, separadas por un punto decimal. Por ejemplo, 3.14, -0.5, 2.0, y 123.456789 son ejemplos de números en coma flotante.


