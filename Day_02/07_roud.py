# En Python, puedes redondear números utilizando la función incorporada round(). La función round() toma dos argumentos: el número que deseas redondear y un segundo argumento opcional que especifica la cantidad de decimales a los que deseas redondear. Si omites el segundo argumento, la función redondeará el número al entero más cercano.

num = 23.777777777

num = round(num)
print(num)

print(round(90 / 7, 2))

round()
