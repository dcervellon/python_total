# En el contexto de programación en Python, las "conversiones" se refieren a la acción de cambiar un tipo de dato o estructura de datos a otro. Python es un lenguaje de tipado dinámico, lo que significa que las conversiones de tipo son comunes y a menudo necesarias para realizar operaciones específicas. Algunos ejemplos comunes de conversiones en Python incluyen:

# Conversión de tipo de dato simple:
# int(): Convierte un valor a un entero.
# float(): Convierte un valor a un número de punto flotante.
# str(): Convierte un valor a una cadena (string).
# bool(): Convierte un valor a un booleano.

num1 = 20
num2 = 30.5

# convert to float
num1 = float(num1)
num2 = str(num2)

print(num1)
print(type(num1))
print(num2)
print(type(num2))
