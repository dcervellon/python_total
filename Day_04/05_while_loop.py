# Un bucle while en Python se utiliza para ejecutar un bloque de código repetidamente mientras se cumple una condición específica. La estructura básica de un bucle while en Python es la siguiente:

# ? Exercise 01
number = 10


while number != -1:
    print(number)
    number -= 1


