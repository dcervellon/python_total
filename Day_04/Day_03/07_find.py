# El método find() en Python se utiliza para encontrar la primera ocurrencia de una subcadena (una cadena más pequeña) dentro de una cadena más grande. Devuelve el índice de la primera posición en la que se encuentra la subcadena o -1 si la subcadena no se encuentra en la cadena más grande.

