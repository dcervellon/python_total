# os operadores de comparación en Python se utilizan para comparar dos valores y devolver un resultado booleano (True o False) que indica si la comparación es verdadera o falsa. Aquí tienes una lista de los operadores de comparación en Python:


# 1. == (Igual a): Comprueba si dos valores son iguales.

comparison_one = 12 == 6 + 6
comparison_two = "hola" == "Hola"
comparison_three = 23 == "23"


# 2. != (Diferente de): Comprueba si dos valores son diferentes.

diference_one = 233 != "233"
diference_two = "hola" != "hola"
diference_three = True != False

# 3. < (Menor que): Comprueba si el valor de la izquierda es menor que el valor de la derecha.

less_one = 23 < 20
less_two = 2000 < 1000 * 2
less_three = 34 < 66 / 1.5


# 4. > (Mayor que): Comprueba si el valor de la izquierda es mayor que el valor de la derecha.

greater_one = 120 > 20
greater_two = 12 > 233
greater_three = -12 > -33


# 5. <= (Menor o igual que): Comprueba si el valor de la izquierda es menor o igual que el valor de la derecha.

less_equal_one = 12 <= 12
less_equal_two = 233 <= 37
less_equal_three = 1000 <= 101010


# 6.>= (Mayor o igual que): Comprueba si el valor de la izquierda es mayor o igual que el valor de la derecha.

greater_equal_one = 12 >= 10
greater_equal_two = 333 >= 333
greater_equal_three = 12 >= 1000
