# ? En Python, una función es un bloque de código reutilizable que realiza una tarea específica. Las funciones pueden tomar argumentos (datos de entrada), realizar alguna operación y devolver un resultado (dato de salida). Aquí hay una descripción general de cómo definir y utilizar funciones en Python:


def greet_person(name):
    """Esta funcion saluda a las personas"""
    print(f"hola {name}")


greet_person("Mateo David")
