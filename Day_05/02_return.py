# El uso de return en Python está relacionado con funciones. La declaración return se utiliza para salir de una función y devolver un valor al llamador de la función. Aquí hay algunas pautas y ejemplos para entender mejor cómo se usa return en Python:


def multiplicar(n1, n2):
    return n1 * n2


result = multiplicar(12, 44)
print(result)
